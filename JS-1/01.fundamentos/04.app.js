/*OPERADORES DE COMPARACION*/  
const num1 = 20;
const num2 = 50;
const num3 = '20';

console.log(num1 > num2);
console.log(num1 < num2);

//comparador de igual
console.log(num1 == num3); //true
console.log(num1 === num3); //false

//diferentes
console.log(20 != 3); //true

console.log(20 == '20'); //true

